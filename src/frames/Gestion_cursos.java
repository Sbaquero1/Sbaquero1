    public Gestion_cursos() {
        initComponents();
        
        this.setLocationRelativeTo(null);
        
        try {
            PreparedStatement ps=cn.prepareStatement("SELECT * FROM curso");
            ResultSet rs=ps.executeQuery();
            
            tabla_gestion_curso=new JTable(model);
            jScrollPane1.setViewportView(tabla_gestion_curso);
            
            model.addColumn("id");
            model.addColumn("curso");
            
            
            while(rs.next()){
            
            Object[] fila=new Object[2];
                for (int i = 0; i < 2; i++) {
                    fila[i]=rs.getObject(i+1);
                }
            
            model.addRow(fila);
            
            }
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error en diligenciando la tabla curso");
        }
        
        
    }
