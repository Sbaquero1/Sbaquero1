    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        
        try {
            if(txtnombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"No puede dejar campos basios");
            
            
            }
            else{
              
                
                
                PreparedStatement ps=cn.prepareStatement("INSERT INTO curso(nombre_curso)Values (?)");
                
                
           
                ps.setString(1,txtnombre.getText());
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Curso agregado con exito");
                
                mostrartabla("");
                limpiar();
            
            }
        } catch (SQLException e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "Problemq al guardar curso..hablar con el administrador");
        }
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
       
        try {
            
            PreparedStatement ps=cn.prepareStatement("update curso set nombre_curso='"+txtnombre.getText()+"'where id_curso='"+txtidcurso.getText()+"'");
            
            int respuesta=ps.executeUpdate();
            if(respuesta>0){
            JOptionPane.showMessageDialog(null,"Cuerso actualizado");
            
            
            }
            else{
            
            JOptionPane.showMessageDialog(null,"No ha selecionado fila");
            
            }
            
        } catch (SQLException e) {
            System.err.println(e);
            
            JOptionPane.showMessageDialog(null,"Error al actualizar curso..Comunciarse con el administrador");
        }
        
        
        
    }//GEN-LAST:event_btnactualizarActionPerformed

